function RingData=reduceBackroundProp(R,PropW)
Prop=0.5;
P1=reshape(PropW,size(PropW,1)*size(PropW,2),1);
R1=reshape(R,size(R,1)*size(R,2),1,3);
[a,~]=find(P1<Prop);
RingData=R1(a,:,:);


                
                